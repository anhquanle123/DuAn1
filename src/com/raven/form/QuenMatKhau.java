    private void btnCapNhatMatKhauMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatMatKhauMoiActionPerformed
        // TODO add your handling code here:
        if (checkTrong()) {
            if (checkMaXacNhan()) {
                repo.updateMatKhau(getForm(), LuuIdQuenMk.getUserId());
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Cập nhật mật khẩu mới thành công");
                dispose();
            } else {
                // Mã xác nhận không khớp
                Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Mã xác nhận không đúng");
            }
        }
    }//GEN-LAST:event_btnCapNhatMatKhauMoiActionPerformed

