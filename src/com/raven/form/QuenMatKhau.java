    public boolean checkTrong() {
        String matKhauMoi = txtMatKhauMoi.getText();
        String maXacNhan = txtMaXacNhan.getText();
        if (matKhauMoi.trim().isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Vui lòng nhập mật khẩu mới!");
            return false;
        } else if (maXacNhan.trim().isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Vui lòng nhập mã xác nhận!");
            return false;
        } else {
            return true;
        }
    }

    public boolean checkMaXacNhan() {
        return txtMaXacNhan.getText().equals(LuuMaXacNhan.getGeneratedCode());
    }

